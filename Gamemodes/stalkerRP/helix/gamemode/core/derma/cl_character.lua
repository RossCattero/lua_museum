-- main character menu panel
PANEL = {}

AccessorFunc(PANEL, "bUsingCharacter", "UsingCharacter", FORCE_BOOL)

function PANEL:Init()
	local sw, sh = ScrW(), ScrH();
	local parent = self:GetParent()
	local bHasCharacter = #ix.characters > 0
	local buttonPadding = ScreenScale(14) * 4

	self.ButtonList = self:Add('DPanel')
	self.ButtonList:SetPos(sw * 0.35, 0)
	self.ButtonList:SetSize(600, sh)
	self.ButtonList.Paint = function(self, w, h)
		surface.SetDrawColor( 255, 255, 255, 255 )
		surface.SetMaterial( ourMat )
		surface.DrawTexturedRect( 0, 0, w, h )
	end;

	local createButton = self.ButtonList:Add("ixMenuButton")
	createButton:SetText("create")
	createButton:SetSize(600, 100)
	createButton:SetContentAlignment(5)
	createButton:SetTextInset(ScreenScale(14) * 4 , 0)
	createButton:SetPos(0, 270)
	createButton.DoClick = function()
		local maximum = hook.Run("GetMaxPlayerCharacter", LocalPlayer()) or ix.config.Get("maxCharacters", 5)
		if (#ix.characters >= maximum) then
			self:GetParent():ShowNotice(3, L("maxCharacters"))
			return
		end

		self:Dim()
		parent.newCharacterPanel:SetActiveSubpanel("faction", 0)
		parent.newCharacterPanel:SlideUp()
	end

	self.loadButton = self.ButtonList:Add("ixMenuButton")
	self.loadButton:SetText("load")
	self.loadButton:SetContentAlignment(5)
	self.loadButton:SetTextInset(ScreenScale(14) * 4 , 0)
	self.loadButton:SetSize(600, 100)
	self.loadButton:SetPos(0, 400)
	self.loadButton.DoClick = function()
		self:Dim()
		parent.loadCharacterPanel:SlideUp()
	end

	if (!bHasCharacter) then
		self.loadButton:SetDisabled(true)
	end

	self.returnButton = self.ButtonList:Add("ixMenuButton")
	self:UpdateReturnButton()
	self.returnButton:SetContentAlignment(5)
	self.returnButton:SetTextInset(numby , 0)
	self.returnButton:SetSize(600, 100)
	self.returnButton:SetPos(0, 530)
	self.returnButton.DoClick = function(self)
		if (LocalPlayer().GetCharacter and LocalPlayer():GetCharacter()) then
			parent:Close()
		else
			RunConsoleCommand("disconnect")
		end
	end
end

function PANEL:UpdateReturnButton(bValue)
	if (bValue == nil) then
		bValue = LocalPlayer().GetCharacter and LocalPlayer():GetCharacter()
	end
	self.returnButton:SetText( (bValue and "return") or "leave")
	self.returnButton:SetSize(600, 100)
end

function PANEL:OnDim()
	self:SetMouseInputEnabled(false)
	self:SetKeyboardInputEnabled(false)
end

function PANEL:OnUndim()
	self:SetMouseInputEnabled(true)
	self:SetKeyboardInputEnabled(true)

	self:UpdateReturnButton(LocalPlayer().GetCharacter and LocalPlayer():GetCharacter())
end

function PANEL:OnClose()
	for _, v in pairs(self:GetChildren()) do
		if (IsValid(v)) then
			v:SetVisible(false)
		end
	end
end

function PANEL:PerformLayout(width, height) end

